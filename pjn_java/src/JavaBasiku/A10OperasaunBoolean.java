package JavaBasiku;

public class A10OperasaunBoolean {
    public static void main(String[] args) {
        //operasaun ne'ebe uza ba data boolean rua
        int a = 10;
        int b = 12;
        int c = 10;
        //&&        // Operator
        //no                   //Keterangan
        //no ne'e bele moss hanesan vezes hotu-hotu tenke loss mak nia foin fo true
        System.out.println(a==c && a<=b);
        //||        // Operator
        //ka                  //Keterangan
        //ka nee mais
        System.out.println(a>=c || a==c);
        //!         // Operator
        //Kebalikan             //Keterangan
        // negasaun
        System.out.println(a!=b);
    }
}
