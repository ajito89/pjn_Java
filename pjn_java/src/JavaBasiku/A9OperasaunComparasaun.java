package JavaBasiku;

public class A9OperasaunComparasaun {
    public static void main(String[] args) {
        int a =10;
        int b = 20;
        int c = 10;
        //operasaun ida ne'e atu halo komparasaun data
        //no nia sempre fo sai valor bollean
        //>                         // Operator
        //Lebih Dari                            //Keterangan
        boolean ab = a > b;
        System.out.println(ab);
        //<                         // Operator
        //Kurang Dari                           //Keterangan
        //>=                        // Operator
        //Lebih Dari Sama Dengan                //Keterangan
        System.out.println(a >= b);
        //<=                        // Operator
        //Kurang Dari Sama Dengan               //Keterangan
        System.out.println(a<=b);
        //==                        // Operator
        //Sama Dengan                           //Keterangan
        System.out.println(a==c);
        //!=                        // Operator
        //Tidak Sama Dengan                     //Keterangan
        boolean ac= a!=c;
        System.out.println(ac);
    }
}
